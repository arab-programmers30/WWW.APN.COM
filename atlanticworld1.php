            <h3>
                Introduction:
            </h3>
            <p>
                The world's second ocean in area after the Pacific Ocean, its area is about 106,400,000 square
                kilometers (41,100,000 sq mi), covering approximately 20% of the Earth's area. <br>
                And 26% of the total water area on Earth, and it is bordered to the east by the continents of Africa and
                Europe, while to the west it is bordered by the Americas. <br>
                Its name “Atlas” came from Greek mythology (in Greek: θάλασσα Ἀτλαντὶς; meaning: the Sea of ​​Atlas)
                approximately in the year 450 BC, that is, before the discovery of the oceans by the Europeans,
                and the term ocean was applied to the waters beyond the Strait of Gibraltar, which is Now the Atlantic
                Ocean. <br>
                The ancient Greeks believed that this ocean was a giant river that encircled the entire world.
                The oldest definition of the word “Atlantic” in the history of Herodotus is approximately in the year
                450 BC: Atlantis Thalassa. <br>
                Before the discovery of other oceans by Europeans, the term “ocean” itself was applied to the waters
                beyond the Strait of Gibraltar, unlike the one we know. <br>
                Now known as the Atlantic, the Arabs in the Middle Ages called it the Sea of ​​Darkness. <br>
                The Atlantic Ocean forms an S-shaped basin that extends longitudinally between the Americas to the west,
                and Eurasia and Africa to the east. <br>
                As a single interconnected global ocean, it is connected in the north by the Arctic Ocean (sometimes
                considered part of the Atlantic Ocean), to the Pacific Ocean to the southwest, the Indian Ocean to the
                southeast, and the Antarctic Ocean to the south. <br>
                (Other information describes the Atlantic Ocean as extending south to Antarctica), and the equator
                divides the ocean into the North Atlantic and the South Atlantic. <br>
                Over the past three decades, ocean winds and waves have become stronger and longer - by up to 5% and 8%,
                respectively - in most regions around the world.
            </p>
            <h3>
                Temperature in the Atlantic Ocean:
            </h3>
            <p>
                Temperatures on the ocean surface vary between 27°C and two degrees below zero. <br>
                The surface temperature is equal to the water mass at a thickness of 30 m to 120 m. <br>
                The water temperature also decreases with increasing depth, while the depth temperature stabilizes at
                the freezing point. <br>
            </p>
            <h3>
                Ocean currents in the Atlantic Ocean:
            </h3>
            <p>
                The sea currents are numerous and move across depths of thousands of metres. <br>
                In the north, the Gulf Stream carries warm water from the tropics to the European coast, and therefore
                ice does not form in most ports of northwestern Europe. <br>
                The Labrador Current originates from the frozen ocean in the north between Greenland and Canada. <br>
                It is a cold current and extends south to the point of convergence with the Gulf Stream, where it ends
                and leads to a cold climate in the northern coast of Canada and the northeastern United States. <br>
                It also brings icebergs from the north, which stand as a stumbling block to Navigation in the area. <br>
                The currents in the southern Atlantic are weaker than those in the north due to the weakness of the
                southern wind systems. <br>
                The Brazil Current carries warm water from the tropics southward across the coast of South America. <br>
                The Falkland Current runs along the coast of Argentina north until it meets the Brazilian Current, which
                is the Falkland Current - a cold current. <br>
                In North Africa, the Benguela Current flows towards the southwestern coast of Africa.
            </p>
            <h3>
                Deep water and bottom water:
            </h3>
            <p>
                The water in the ocean freezes due to the cold climate near Antarctica, and the ice here is fresh even
                though the sea water is salty. <br>
                The result is that the water that has not frozen contains large amounts of salt, and it remains around
                the ice mass in a very cold state. <br>
                Its density increases due to the abundance of salt in it and the cold weather around it. <br>
                This heavy water in the sea sinks to the bottom and spreads to distant areas. <br>
                Thus, scientists learned about the cold Antarctic waters in upstate New York, USA. <br>
                Deepwater emerging from the North Atlantic Ocean is formed in several ways: High-salt water from the
                Gulf Stream mixes with cold water from Arctic Greenland, and in winter, winds blow and this mixed water
                grows colder and then sinks to the bottom. <br>
                It flows over the bottom waters towards the south around Antarctica. It is what is called deep water.
                <br>
                The Atlantic Ocean has another source of deep water: It is the Mediterranean Sea, and its water is
                saltier than ocean water due to intense evaporation. <br>
                The waters of the Mediterranean Sea flow into the ocean through the Strait of Gibraltar and not the
                other way around, due to the density of salt in the Mediterranean waters. <br>
                It dives to depths of more than 910 metres. From there, it spreads out in a thin slice over the dense
                waters of Greenland.
            </p>
            <h3>
                The biome of the Atlantic Ocean:
            </h3>
            <p>
                The Atlantic Ocean, over its vast area, harbors various fish and marine animals that feed on plants
                accumulated at the bottom, and other plants float on the surface of the water to take their need of
                light. <br>
                The surface waters adjacent to the continental shelves contain important elements of minerals, the
                presence of which contributes to the flourishing of plants, and in their vicinity the reproduction of
                fish and other animals. <br>
                And so on the West African coast, and in the farthest regions north and south. <br>
                Ocean currents lift minerals from depth to the surface. <br>
                The Atlantic Ocean supplies the world with approximately one-third of its total fish and shellfish
                catches. <br>
                Most of the marine resources are found in the surface waters of the Dogger Cliff in the North Sea
                region, and the southeastern regions of Newfoundland and Canada.
            </p>
