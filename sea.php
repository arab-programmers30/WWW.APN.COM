    <p style="padding-left: 10px;">
        Ocean parks are areas rich in marine life characterized by their environmental and biological diversity and
        found beneath the surface of the ocean. <br>
        Coral reefs, seaweeds, rocky reefs, coral and various marine organisms are part of these parks. <br>
        These areas are considered important ecological sites that play a major role in preserving marine biodiversity
        and providing refuge and resources for marine organisms. <br>
        Ocean parks are located in many regions around the world, and they include such areas as: <br>
    <ol style="padding-left: 10px;">
        <li>
            Coral reefs: <br>
            Coral reefs are an important biological environment that provides shelter for many marine organisms. <br>
            These reefs also provide a source of food and support for marine fishing.
        </li>
        <li>
            Deep coral: <br>
            In addition to pelagic corals, there are also corals that grow in deep waters and contribute to providing a
            habitat for marine organisms.
        </li>
        <li>
            Rocky reefs: <br>
            These areas include rocky formations on the ocean floor that provide habitats for marine organisms to live
            and reproduce.
        </li>
        <li>
            Seagrasses: <br>
            These marine plants are found in shallow waters and provide a place for marine organisms to feed and
            reproduce.
        </li>
    </ol>
    </p>
    <p style="padding-left: 10px;">
