            <h3>
                Introduction:
            </h3>
            <p>
                The Indian Ocean is the third largest ocean in the world in terms of area after the Pacific and Atlantic
                Oceans,
                with an area of ​​about 74 million km². <br>
                Its area is less than half the area of ​​the Pacific Ocean. <br>
                The Indian Ocean overlooks the Red Sea, the oil-rich Persian Gulf, the Arabian Sea, the Andaman Sea, and
                the Bay of Bengal. <br>
                The Indian Ocean extends from Africa in the west to Australia and Indonesia in the east,
                and from Asia in the north to Antarctica in the south. <br>
                The width of the ocean between the southernmost tip of Africa and Australia is about 9,980 km,
                and its width decreases as we go north, as India and Sri Lanka divide it into two parts, namely the
                Arabian Sea and the Bay of Bengal. <br>
                The average depth of the Indian Ocean is 3,897 metres, while its deepest known points reach 7,725
                metres. <br>
                Since ancient times, the Indian Ocean has been one of the most important trade routes. <br>
                The Arabs, Chinese, and Indians have used it in their trade for hundreds of years. <br>
                In the year 1498 AD, the Portuguese explorer Vasco Dagama, with the help of the Arab navigator Ibn
                Majid,
                crossed the Indian Ocean after circling the southern tip of Africa.<br>
                After the opening of the Suez Canal in 1869, the Indian Ocean became the main trade route between Europe
                and the Far East.
            </p>
            <h3>
                Formative plates:
            </h3>
            <p>
                Its floor is occupied by the Indian, African and Antarctic tectonic plates,
                and its place of meeting is occupied by Y-shaped oceanic ridges. <br>
                Its trunk juts out from Mumbai, dividing the ocean into three basins:
            <ul>
                <li>
                    Eastern (Bengal)
                </li>
                <li>
                    Western (Arabic)
                </li>
                <li>
                    southern
                </li>
            </ul>
            The mountains of its main oceanic ridge are characterized by their seismic activity,
            and they branch toward the southwest and southeast in the middle of the ocean,
            including the Ninety East Ridge, which trends north-south over a length of 4,500 km at 90 degrees east
            longitude,<br> its continental shelves are narrow,
            ranging in width from 120 km to 200 km, with the exception of western Australia. <br>
            Its average depth is 3890 m, and its deepest point is located at the Java Trench, whose depth is estimated
            at about 7450 m. <br>
            Its bottom, north of 50 degrees south latitude, is covered by pelagic sediments,
            about half of which (47%) consists of fragments of calcareous globigerine shells,
            and about a fifth (22%) of diatomite and marine clay,
            14% of which is covered by iron sediments, and the remaining 17% is dominated by belt lands and inert
            deposits in the far southern latitudes. <br>
            In the spring of 2000, the area extending to its south, between Antarctica and 60° south latitude,
            was designated the fifth southern ocean by the International Hydrographic Organization, yet it maintained
            its third rank among the oceans. <br>
            Its most important straits and side seas are:
            <ul>
                <li>
                    Bab al-Mandab
                </li>
                <li>
                    Suez Canal
                </li>
                <li>
                    Strait of Hormuz
                </li>
                <li>
                    Malaga Strait
                </li>
                <li>
                    Lombok Strait
                </li>
            </ul>
            As for his sailors, they are:
            <ul>
                <li>
                    Andaman
                </li>
                <li>
                    The Arabian Sea
                </li>
                <li>
                    Bay of Bengal
                </li>
                <li>
                    Australian Bight
                </li>
                <li>
                    Gulf of Aden
                </li>
                <li>
                    Laccadive Sea
                </li>
                <li>
                    Mozambique Channel
                </li>
                <li>
                    The Red Sea
                </li>
                <li>
                    The Arabian Gulf
                </li>
            </ul>
            </p>
            <h3>
                Geographical location:
            </h3>
            <p>
                The Indonesian Sunda Islands separate the Indian and Pacific Oceans,
                and some waterways separate these islands, such as the Strait of Malacca,
                the Sunda Strait, and the Timor Sea. <br>
                The Indian and Atlantic oceans meet at the southern tip of Africa,
                and the waters of the tropical Indian Ocean meet the cold waters of the Antarctic Ocean at 55 degrees
                latitude. <br>
                ° South in an area called the polar mixing zone.
                The Suez Canal connects the Red Sea to the Indian Ocean arm of the Mediterranean Sea. <br>
                The most important ports in the Indian Ocean are:
