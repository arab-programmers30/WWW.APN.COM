            <h3>
                Climate in the Indian Ocean:
            </h3>
            <p>
                Most of the Indian Ocean - especially its central and northern ones - has a tropical climate. <br>
                For this reason, the ocean waters are almost free of icebergs, thick fog, and other navigational
                hazards. <br>
                The temperature over the waters of the Indian Ocean varies greatly between the months of January and
                July,
                but this variation does not reach the variation we find in both the Atlantic and Pacific oceans.<br>
                Its climate is dominated by southern and westerly winds between October and April and from May until
                October. <br>
                The turbulent monsoons in the Arabian Sea also deliver rain to the Indian subcontinent. <br>
                Winds in the Southern Hemisphere are generally milder,
                while summers can be windy and severe near Mauritius. <br>
                Cyclones can also hit the shores of the Arabian Sea and the Bay of Bengal during the change in the
                direction of the monsoon winds. <br>
                These winds are generally hydrologically controlled by the two main circulation patterns of ocean
                currents. <br>
                It flows, as usual, in the Northern Hemisphere in a clockwise direction, and in the south of the
                equator, in the opposite direction. <br>
                The currents reverse in the north during the winter seasonal seasons, and the waters entering from the
                Atlantic Ocean, the Red Sea, and the Antarctic Current control the deep water cycle,
                when the minimum temperature of surface water is north of latitude 20 degrees south. <br>
                About 22 degrees Celsius, the surface water temperature exceeds 28 degrees in the east; <br>
                It suddenly descends south of latitude 40° south. <br>
                Icebergs and icebergs appear throughout the year south of latitude 65° south. <br>
                The average range of its movements in March can reach 50 degrees south latitude, and in October up to 58
                degrees south latitude. <br>
                Salinity at the surface varies between 0.37% in the tropical north and 0.32% near the Arctic Circle.
                <br>
                It is most intense in the Arabian Sea, the Red Sea, and in the strip extending between southern Africa
                and southwestern Australia. <br>
                Tide height ranges between about half a meter in Fremantle and 11 meters in Collier Bay, northwestern
                Australia. <br>
                Some abundant rivers flow into it, such as:
            <ul>
                <li>
                    Ganges River
                </li>
                <li>
                    Brahmaputra River
                </li>
                <li>
                    Indus River
                </li>
                <li>
                    Shatt al-Arab River
                </li>
                <li>
                    Zambezi River
                </li>
                <li>
                    Irawadi River
                </li>
                <li>
                    Salween River
                </li>
            </ul>
            Its estuaries are not suitable for navigation or crossing into the continent due to the large amount of
            sediments deposited in its courses.
            </p>
            <h3>
                Temperature in the Indian Ocean:
            </h3>
            <p>
                During the month of January, north of the equator, it ranges between 21°C and 27°C,
                in the area surrounding the Arabian Gulf and the Red Sea. <br>
                The temperature sometimes rises to 32°C. In the southern half of the ocean, the temperature ranges
                between 27°C at the equator and 13°C between southern Australia and southern Africa. <br>
                But it reaches below minus 1 degree Celsius near Antarctica in July.
            </p>
            <h3>
                Bathymetric map of the Indian Ocean:
            </h3>
            <p>
                The Indian Ocean is divided by a Y-shaped ridge at its midpoint into two parts. <br>
                This edge extends from the south of the Arabian Sea, to the tip of Antarctica, and several conical
                mountains emerge from it above the surface of the water,
                at the southern end of this edge. These prominent mountains form islands such as the Cocos Islands,
                located south of Indonesia, and Tromelin Island, east of Madagascar. <br>
                The floor in the middle of the ocean sometimes rises to form shallow areas called shoals,
                such as the Genista Range, off the coast of the Arabian Peninsula, and the Hellbank between Madagascar
                and Mozambique. <br>
                At the bottom of the Atlantic Ocean there are several canyons called the Aghwar, the deepest of which is
                the Java Groove, about 7.7 km long. <br>
                Earthquakes occur, most of which occur along the middle of the rim, in the middle of the Indian Ocean
                and in the Arabian Sea, and from Burma to approximately western New Guinea.
                Based on the theory of continental creep, the Indian Ocean may have begun to form 200 million years ago,
                when a land mass called Pangea began to break apart, leading to the formation of several continents.
                <br>
                India was not part of the continent of Asia at that time, but it was located next to the South Pole,
                and it settled in its current location as part of Asia 50 million years ago as a result of creeping
                northward at an annual speed of up to five centimetres. <br>
                Scientists believe the continent's advance continued along the Y-shaped ridge, pushing basement rocks
                toward its sides at an annual rate of 1-2 cm. <br>
                The degree of pollution in the Indian Ocean has increased due to oil leakage from giant tankers, as well
                as waste from metal and chemical industries and wastewater. <br>
                As for the Arabian Gulf, pollution increased during the Second Gulf War in 1991 after Iraq emptied about
                1.75 billion liters of oil into it.
            </p>
            <h3>
                Winds in the Indian Ocean:
            </h3>
            <p>
                There are three bands of winds over the Indian Ocean:
            <ul>
                <li>
                    Monsoon winds
                </li>
                <li>
                    Southeastern trade winds
                </li>
                <li>
                    Permanent westerly winds
                </li>
            </ul>
            The northeastern (dry) monsoon winds blow from Asia through India, to eastern Africa, in the period between
            October and April. <br>
            The southwest (wet) monsoon winds blow from the Arabian Sea and the Bay of Bengal across India and southwest
            Asia, carrying moisture from ocean water. <br>
            As for the permanent westerly winds, they blow from west to east permanently. <br>
            These winds are often accompanied by violent hurricanes that strike Madagascar and other regions of the
            Indian Ocean.
            </p>
            <h3>
                Ocean currents in the Indian Ocean:
            </h3>
            <p>
                Sea currents and tides are affected by winds and vary depending on the seasons. <br>
                North of the equator, huge masses of water move towards the east or west, depending on the type of
                monsoon winds. <br>
                While the warm sea currents move south of the equator west towards Africa, then head south along its
                eastern coast,
                and then change their direction towards the east, moving with the direction of the westerly winds to
                Australia. <br>
                These westerly winds are accompanied by a cold water current pushed into the region by polar winds that
                blow north towards Australia when they change their direction towards the east. <br>
                The extent of high tide in the Indian Ocean varies greatly, but this difference remains less than in the
                Atlantic and Pacific oceans. <br>
                The Indian Ocean is smaller in area and surrounded by land, which makes the tidal movement in it
                moderate. <br>
                The highest tide on the Western Australian coast, reaching 11 m, occurs at Collier Bay near Derby, but
                at Fremantle it rises only 0.6 m.
            </p>
            <h3>
                Inhabitants of the Indian Ocean:
            </h3>
            <p>
                The population of the Indian Ocean depends economically on the main sea route, which is served by about
                15% of the ships of the world fleet. <br>
                Especially since it connects the Middle East, Africa, and East Asia to Europe and the Americas. <br>
                Through it, very large quantities of oil and its products are transported, which are extracted from its
                fields located on the continental threshold off the shores of the Arabian Gulf (the Gulf states and
                Iran),
                Indonesia, and western Australia. Large reserves of hydrogen coal (oil and gas) are still being
                discovered on its sidewalks,
                and their production is generally estimated at about 40% of global production. <br>
                Its sands, opposite some mineral beaches, also contain local and nodal deposits of iron, manganese, and
                copper compounds. <br>
                It actively invests in periphery countries, particularly in India, South Africa, Indonesia, Sri
                Lanka and Thailand.
            </p>
            <h3>
                Marine life in the Indian Ocean:
            </h3>
            <p>
                The Indian Ocean is rich in fish and shellfish, but is less important for fish trade than its Atlantic
                and Pacific counterparts because of its tropical climate, where fish spoil quickly unless treated or
                refrigerated promptly. <br>
                The Indian Ocean contributes only 5% of the world's total fish catch. <br>
                The amount of fish from the Indian Ocean reaches about 3.86 million metric tons annually, and most of
                the fishing is concentrated on the western coast of India,
                but fishing stops during the months of June, July and August due to the turbulence of the ocean waters
                due to the blowing of the south-west monsoon winds. <br>
                The temperature of the waters of the Indian Ocean keeps the productivity of oceanic plankton low, with
                the exception of extensions of the northern margins and a few scattered places. <br>
                Thus, biological production in this ocean is limited and fishing production falls to low levels,
                estimated at about 5% of the global catch, despite the growing need of the surrounding countries. <br>
                It is used to fish for food consumption and export. <br>
                Indian fishing is concentrated on the western coast, and stops in the summer due to weather disturbances
                and ocean waters. <br>
                Some fishing fleets come from Russia, Japan, South Korea, and Taiwan to invest in its tons, prawns, and
                shrimp. <br>
                There are many marine species at risk of extinction, such as turtles, whales and seals. Fishing is
                usually concentrated either near the equator or in the high latitudes. <br>
                Oil pollution has begun to dominate, to varying degrees, the waters of the oil-producing countries’
                beaches and transit routes, which increases the dangers to which their neighborhoods are exposed.
            </p>
            <h3>
                History of the Indian Ocean:
            </h3>
            <p>
                The history of the peoples of the Indian Ocean goes back to the shores around it where the oldest
                civilizations appeared along the valleys of the Tigris, Euphrates, Nile, and Indus... starting with
                Sumer, ancient Egypt, and the Indian subcontinent. <br>
                It was followed by Persian civilizations in Elam, and then in Southeast Asia, starting with Funan. <br>
                In about 3000 BC, in the days of the First Dynasty in Egypt, a group of sailors was sent on a foreign
                trip to the country of Punt, which is believed to be part of present-day Somalia, and the sailors
                returned. <br>
                Loaded with gold and gum, the first commercial voyages between the two rivers and the Indus Valley took
                place along the shores of the Indian Ocean around the year 2500 BC. <br>
                Perhaps the Phoenicians entered the region in the third millennium BC without leaving any indication of
                their places of settlement. <br>
                Trade was active in the Indian Ocean before other oceans due to its relative calm. <br>
                Despite the violence and strength of the seasonal seasons, ordinary sea vessels were able to avoid them
                by sailing west before the seasons, and then waiting a few months to return to sailing east. <br>
                This is what allowed the Indonesians to cross the Indian Ocean to settle in Madagascar. <br>
                Between the first and second centuries BC, the Greek Eudoxus was one of the first Greeks to cross the
                ocean, it is also said that Hyppalus was at the same time the first to discover the direct route between
                the Arabian Peninsula and India. <br>
                Trade was also active in the first and second centuries BC. <br>
                Trade between Roman Egypt and the Tamil kingdoms of Cheras, Cholas, and Pandyas in southern India was
                also active, as was the case with It concerns Indonesians, and Western sailors also took advantage of
                the monsoons to cross the ocean, one of the sailors, around the year 71 AD, explained the ports he
                encountered on his way and the goods transported along the Indian and African shores. <br>
                Between the years 1405-1433, the sea prince, Zheng He, led a huge fleet of the Ming Dynasty to the
                Western (Indian) Ocean, and reached the shores of the countries of East Africa. <br>
                By 1497, Vasco da Gama circumnavigated the Cape of Good Hope and became the first European to sail to
                India with the help of the famous Arab traveler Ibn Majid. <br>
                Then European ships, armed with powerful cannons, quickly took control of trade. <br>
                Portugal was the first to build castles in important ports and passages, without being able - as a small
                country - to protect such a vast project؛ Therefore, in the middle of the seventeenth century, it was
                replaced by larger European powers, such as the Dutch East India Company - between 1602 and 1798 - which
                focused its interests on managing and controlling trade with the East across the Indian Ocean. <br>
                Britain and France then established competing trading companies for the same purpose, after which, in
                1815, Britain became the largest major power and dominated the region. <br>
                With the opening of the Suez Canal in 1869, European interest in the East revived without any people
                achieving a successful commercial achievement that achieved sovereignty. <br>
                Since World War II, Britain has partially withdrawn from the region and focused its attention only on
                India. <br>
                At the same time, the United States and the Soviet Union were searching for naval bases and military
                sites for each of them to control, while the peoples and countries of the region surrounding the Indian
                Ocean were striving to liberate it and make it a peace zone so that they could sail through it in peace.
                <br>
                So the United Kingdom and the United States focused on their war bases on the Diego Garcia Atoll in the
                middle of the Indian Ocean. <br>
                All attempts by colonial countries to maintain their colonies failed, and so the Indian Ocean became the
                backbone of many political independence demonstrations in India and elsewhere, and thus India achieved
                its independence from Britain. <br>
                Indonesia and others also led the camp of non-aligned countries at the Bandung Conference. <br>
                In December 2004, most of the inhabitants of the coast of the Indian Ocean, from the easternmost to the
                westernmost reaches, suffered the blow of a massive tsunami due to a deep earthquake that claimed the
                lives of about 250,000 victims and the loss of more than a million people of their homes.
            </p>
