                <h1>
                    The importance of the oceans: water worlds full of surprises
                </h1>
            </center>
            <h3>
                <b>
                    Article writer:
                </b>
                Rasha Adel
            </h3>
            <p>
                Hello little friends!<br>
                Have you ever wondered about the secret of the endless oceans and their great importance in our lives?
                In this article, we will take you on a quick journey through the worlds of the deep oceans to discover
                how important they are and their great impact on planet Earth and our lives.
            </p>
            <h3>
                Oceans and oceans are the source of life:
            </h3>
            <p>
                The oceans and oceans are some of the largest places on Earth and are home to a variety of amazing
                marine creatures. There are countless species of fish, beautiful coral reefs, and giant whales. These
                organisms form a complex food chain that affects other organisms on land.
            </p>
            <h3>
                Regulating the Earth’s climate:
            </h3>
            <p>
                Oceans help regulate Earth's climate. The oceans absorb and store a lot of heat from the sun, which
                helps reduce atmospheric fluctuations and moderate temperatures on Earth's surface. This helps prevent
                excessive climate changes.
            </p>
            <h3>
                Oxygen production:
            </h3>
            <p>
                Half of the oxygen we breathe depends on the oceans. Algae and marine life produce oxygen through a
                process called photodeposition. This makes the oceans an important bioenergetic resource for our lives.
            </p>
            <h3>
                Coastal protection:
            </h3>
            <p>
                Oceans act as a natural barrier that protects coastlines from erosion and strong waves. If there were no
                oceans, coastlines would be more vulnerable to damage and destruction.
            </p>
            <h3>
                Economy:
            </h3>
            <p>
                The oceans provide many economic opportunities. Many people live on the coasts and depend on fishing and
                marine tourism for their livelihood. In addition, oil and natural gas are extracted from the bottom of
                the oceans, providing fuel and energy to the world.
            </p>
            <center>
                <h1>
                    Main  Knowledge bank - Children's World Department The difference between the sea and the ocean for
                    children and the types of oceans and seas
                </h1>
            </center>
            <h3>
                 The difference between the sea and the ocean for children and the types of oceans and seas:
            </h3>
            <p>
                Here is an explanation of the difference between the sea and the ocean for children in detail, and with
                pictures in an easy and fun way. You will also learn about the types of seas in the world, the types of
                oceans on Earth, how many there are, and more useful information. Enjoy reading.
            </p>
            <h3>
                Definition of sea and ocean:
            </h3>
            <p>
                The sea is part of a large body of water that covers a large part of the Earth's surface, and is
                connected to the oceans. The sea is characterized by the diversity of its marine life, its close banks,
                its medium depth, and its salty water.
                <br>
                As for the ocean, it is a vast and very deep body of water that covers about two-thirds of the Earth’s
                surface and connects the continents and seas through the continental edges. The oceans are characterized
                by salty water, huge whales, and sea currents, which affect the global climate and the organization of
                the world's ecosystems.
                <br>
                Both the sea and the ocean are of great importance to life on Earth as they represent a major source of
                food and natural resources such as oil, gas and minerals, and they also greatly influence the world's
                climate, weather and biodiversity.
            </p>
            <h3>
                Explain the difference between sea and ocean for children:
            </h3>
            <p>
                Sea and ocean are two words used to describe large areas of water above the Earth, but there are some
                differences between them, which are as follows:
            </p>
            <h3>
                Area: 
            </h3>
            <p>
                The ocean is the largest water area on the surface of the Earth, constituting more than 70% of the
                Earth’s surface area. Oceans surround the continents, so they are called oceans, while the sea is the
                small water area located between the continents only and has an entrance from the ocean or without an
                entrance.
            </p>
            <h3>
                Depth: 
            </h3>
            <p>
                The ocean is much deeper than the sea, with some parts of the ocean exceeding 100,000 feet deep, while
                the deepest sea in the world reaches 36,000 feet.
            </p>
            <h3>
                Waves: 
            </h3>
            <p>
                Waves in the ocean are larger and stronger than waves in the sea, due to the large size of the ocean and
                the influence of winds and currents.
            </p>
            <h3>
                Living organisms:
            </h3>
            <p>
                Both the ocean and the sea contain a wide variety of living organisms, but the ocean contains more
                diverse and larger creatures, such as the blue whale, which is the largest creature on Earth, and there
                are rags and other strange creatures that live in the distant and difficult-to-reach depths.
            </p>
            <h3>
                Location: 
            </h3>
            <p>
                The ocean is characterized by the fact that it surrounds the continents and extends on their sides,
                while the sea exists only between the continents and countries.
            </p>
            <p>
                In general, it can be said that the ocean is the largest and deepest water area, while the sea is the
                limited water area located between some continents, but both contain a wide variety of fish, organisms
                and important natural resources.
            </p>
            <p>
                This information can be useful to school students in comparing seas and oceans in a table for the first
                year of average.
            </p>
            <center>
                <h1>
                    Ocean types 
                </h1>
            </center>
            <p>
                There are five oceans on the Earth's surface, and these oceans constitute the largest part of the water
                area on Earth. which is next:
                <br>
            <ul>
                <li>
                    The Pacific Ocean: <br>
                     It is the largest ocean in the world and extends over an area estimated at about 63.8 million
                    square miles, and is bordered by Asia, Australia, South and North America, and Antarctica.
                </li>
                <li>
                    The Atlantic Ocean: <br>
                     It is the second largest ocean in terms of size and extends over an area estimated at about 31.8
                    million square miles, and is bordered by Europe, Africa, North America, and South America.
                </li>
                <li>
                    The Indian Ocean: <br>
                    It is the third largest ocean in terms of size and extends over an area estimated at about 26.5
                    million square miles, and is bordered by Asia, Australia, Africa, and the Southern Ocean.
                </li>
                <li>
                    The Antarctic Ocean: <br>
                    It is also known as the Southern Ocean and extends around Antarctica, and is bordered by the
                    Pacific, Atlantic, and Indian oceans.
                </li>
                <li>
                    The Arctic Ocean: <br>
                    It is also known as the North Sea and extends around the North Pole, and is bordered by the northern
                    continents of Europe, Asia, and North America.
                </li>
            </ul>
            </p>
            <p>
                The Pacific Ocean is the largest and deepest ocean, while the Atlantic Ocean is the ocean most crowded
                with ships and economic activity. 
                <br>
                The oceans play an important role in regulating the global climate and providing important natural
                resources, such as fish, oil and natural gas.
            </p>
            <h1>
                Sea types
            </h1>
            <p>
                There are many seas around the world, and they differ in size, location, and geographical and
                environmental characteristics. Among the most important global seas can be mentioned:
            </p>
            <p>
            <ul>
                <li>
                    The Mediterranean Sea: <br>
                    The Mediterranean Sea is a warm sea located between Europe, West Asia, and North Africa, and is
                    famous for its rich history and the beauty of its beaches. 
                </li>
                <li>
                    The Red Sea: <br>
                    It is located between the continents of Asia and Africa and is considered one of the hottest seas in
                    the world, and is famous for the diversity of its unique marine life.
                </li>
                <li>
                    The Black Sea: <br>
                    The Black Sea, which was previously called the Sea of ​​Pontus, is an inland sea located between the
                    southeastern part of Europe and Asia Minor. It is connected to the Mediterranean Sea through the
                    Bosphorus Strait and the Sea of ​​Marmara.
                </li>
                <li>
                    The Aegean Sea : <br>
                    It is a sea located between Turkey and Greece, and is connected to the Mediterranean Sea. Its length
                    is 644 km, its width is 322 km, and it contains many small islands. Its ancient name is Archipelago.
                </li>
                <li>
                    Dead Sea:<br>
                     The Dead Sea is an enclosed, very salty sea, located in the Jordan Valley canyon, on the border
                    line separating Jordan and historic Palestine.
                </li>
                <li>
                    The Caribbean Sea: <br>
                    It is located in northern South and Central America and eastern North America and is famous for the
                    beauty of its sandy beaches and the diversity of its marine life.
                </li>
                <li>
                    The Sea of ​​Japan: <br>
                    It is one of the most active seas on the economic, cultural and tourism levels, and is famous for
                    its wealth of fish and its cold climate.
                </li>
                <li>
                    The Baltic Sea: <br>
                    It is located in northern Europe, surrounded by the Scandinavian Peninsula, Central Europe, Northern
                    Europe, Eastern Europe, and the Danish islands. It is famous for the beauty of its natural scenery
                    and the diversity of its marine life.
                </li>
            </ul>
            </p>
            <p>
                There are many other seas that form part of the picturesque nature in different parts of the world.
            </p>
            <h3>
                Which is larger, the ocean or the sea?
            </h3>
            <p>
                The ocean is larger than the sea, because oceans extend over very wide areas and connect several
                continents, while seas are smaller in area and connect only two or three continents.
            </p>
            <h3>
                Are there 5 or 7 oceans?
            </h3>
            <p>
                While originally there is only one global ocean surrounding the continents on each side, the vast body
                of water covering 71 percent of the Earth is geographically divided into 5 specific geographic regions.
            </p>
