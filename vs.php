            <h3>
                Land gardens and ocean gardens are two terms used to describe architectural designs and concepts in the
                field of architecture and urban planning. The difference between them can be summarized as follows:
            </h3>
            <ol>
                <li>
                    Location:
                    <ul>
                        <li>
                            Land gardens: means green spaces, parks, and gardens that are on land, and depend on
                            environmental planning and design in cities and urban areas.
                        </li>
                        <li>
                            Ocean Parks: These mean green spaces and recreational areas that are located on the sea
                            coasts or oceans. These areas are often interconnected with water and contain design
                            features that engage with the marine environment.
                        </li>
                    </ul>
                </li>
                <li>
                    Objective:
                    <ul>
                        <li>
                            Land parks: Land parks are designed to provide green spaces for rest and recreation for
                            local residents and visitors to the city. They usually contain walking paths, seating areas,
                            children's games, places to exercise and other recreational facilities.
                        </li>
                        <li>
                            Land parks: Land parks are designed to provide green spaces for rest and recreation for
                            local residents and visitors to the city. They usually contain walking paths, seating areas,
                            children's games, places to exercise and other recreational facilities.
                        </li>
                        <li>
                            Ocean Gardens: Ocean Gardens are designed to provide opportunities to enjoy seascapes and
                            water activities. These parks may include areas for swimming, canoeing, fishing, and natural
                            observation of the ocean and marine creatures.
                        </li>
                    </ul>
                </li>
                <li>
                    Environment:
                    <ul>
                        <li>
                            Earth gardens: depend on the dry environment and are exposed to wild weather conditions and
                            local climate changes.
                        </li>
                        <li>
                            Ocean parks: They are greatly affected by the marine environment, and the influence of tides
                            and other marine environmental factors must be taken into account in the design and
                            organization processes.
                        </li>
                    </ul>
                </li>
                <li>
                    Activities:
                    <ul>
                        <li>
                            Land parks: encourage land activities such as walking, cycling, barbecuing and sports on the
                            grass.
                        </li>
                        <li>
                            Ocean Parks: Encourages water activities such as swimming, surfing, diving and fishing.
                        </li>
                    </ul>
                </li>
            </ol>
            In short, land parks focus on enjoying land green spaces in an urban environment, while ocean parks are
            associated with water and activities
