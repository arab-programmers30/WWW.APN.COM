            <h1>Arctic World</h1>
            <h2>Exploring the Arctic Ocean</h2>
            <h3>
                <b>
                    Article writer:
                </b>
                Fatma Ahmed Abdelzaher
            </h3>
            <h4>
                Source: World atlas
            </h4>
            <h3>
                Where did the name come from and what does it mean?
            </h3>
            <p>
                The Arctic Ocean gets its name from the Greek word Arktikos, which means bear. <br>
                This name in turn is taken from the northern constellation “Ursa Major,” which means the Great Bear.
                <br>
                This constellation can be constantly seen across the sky in the northern hemisphere and is one of the
                most famous constellations.
            </p>
            <h3>
                Area of the Arctic Ocean:
            </h3>
            <p>
                It is the smallest of the five major oceans. <br>
                It covers a total area of 5,427 million square miles, making it roughly equivalent to the land area of
                Russia. <br>
                It also covers 14,090,000 square kilometers, or less than a tenth of the area of the largest ocean (the
                Pacific). <br>
                Despite this size, the Arctic Ocean covers less than 3% of the Earth’s surface.
            </p>
            <h3>
                Where is the Arctic Ocean located?
            </h3>
            <p>
                The Arctic Ocean is located entirely within the Arctic Circle and is bordered by the US state of Alaska,
                Canada, Russia, Iceland, Norway and Greenland. <br>
                This ocean is located directly north of latitude 60 degrees north and is home to the North Pole.
            </p>
            <h3>
                Underwater edges in the Arctic Ocean:
            </h3>
            <p>
                This frozen ocean is divided into two main basins and four sub-basins by underwater ridges: <br>
            <ul>
                <li>
                    The Alpha
                </li>
                <li>
                    The Gakkel
                </li>
                <li>
                    The Lomonosov
                </li>
            </ul>
            The Lomonosov Ridge divides the floor of the Arctic Ocean into two basins, the Amerasian and the Eurasian,
            whose depths range between 13,000 and 14,800 feet. <br>
            This point is much deeper than most oceans, whose average depth is only 3,406 feet. <br>
            The Alpha Rim forms a basin that extends from its borders to the coastal regions of Alaska and Canada. <br>
            This basin is known as the Canada Basin, and it also forms the Markov Basin with the Lomonosov Rim. <br>
            As for the edge of Gakkel, it forms the Nansen Basin between its borders and the continental shelf. <br>
            Additionally it forms the Amundsen basin with the Lomonosov ridge.
            </p>
            <h3>
                Islands in the Arctic Ocean:
            </h3>
            <p>
                The Arctic Ocean is home to many islands, including the New Siberian Islands, Novaya Zemlya, Wrangel
                Island, and the Arctic Archipelago. <br>
                The New Siberian Islands are an archipelago chain of approximately 11,000 square miles. <br>
                These islands are home to a weather station and Russia's naval base. <br>
                As for the Novaya Zemlya area, it covers about 35,000 square miles and is also considered an
                archipelago. <br>
                It is home to a small group of people who survive by fishing and trapping. <br>
                Wrangell has an area of 1,740 square miles and is home to a small permanent population as well as an
                Arctic research station. <br>
                Finally, the Arctic Archipelago consists of more than 50 small islands, including Prince of Wales,
                Baffin, Victoria, Somerset, Ellesmere, and Banks.
            </p>
            <h3>
                What is the temperature in the Arctic Ocean?
            </h3>
            <p>
                The ocean temperature remains around 28 degrees Fahrenheit year round, regardless of the season, but
                this consistency has been changing over the past few years, as global climate change has become more
                pronounced. <br>
                This phenomenon contributes to an increase in the temperature in the ocean water, which increases the
                temperature of the water, which in turn causes more ice to melt during the summer and reduces its
                freezing point in the winter. <br>
                The winter season is longer than in other regions of the world, which affects the climate over the
                ocean.
            </p>
            <h3>
                Ice in the Arctic Ocean:
            </h3>
            <p>
                The Arctic Ocean is either partially or almost completely covered with ice, depending on the season.
                <br>
                Between October and June, the ice cover over the ocean was so thick that ships could not pass. <br>
                This ice occurs in three forms:
            <ol>
                <li>
                    Polar
                </li>
                <li>
                    Firm
                </li>
                <li>
                    Fast
                </li>
            </ol>
            <p>
                1- Polar ice: is found in the ocean throughout the year and does not melt completely. <br>
                During the summer, its thickness is about 6.56 feet, but during the winter, its thickness reaches 164
                feet.
            </p>
            <p>
                2- The packages are located around the edges of the polar ice, and are completely frozen during the
                winter. <br>
                This ice does not float in separate pieces. <br>
                These pieces are pushed by the wind until they form an almost solid piece.
            </p>
            <p>
                3-Quick ice: It forms around ice packs and the edges of land masses. <br>
                Fast ice, unlike pack ice, does not move.
            </p>
            </p>
            <h3>
                Icebergs in the Arctic:
            </h3>
            <p>
                Many icebergs can be found in the ocean. <br>
                After being freed from the ice masses surrounding them. <br>
                Most of them come from northeastern Canada, western Greenland, and the Ellesmere Islands. <br>
                The presence of icebergs may have a serious impact on ships passing through the ocean and are known to
                cause shipwrecks. <br>
                One of the largest known ships that were wrecked by icebergs was the Titanium.
            </p>
            <h3>
                Degree of salinity in the ocean:
            </h3>
            <p>
                The ocean is fed by a number of freshwater rivers and other bodies of water and melted ice. <br>
                This fresh water mixes with the salty water of the ocean, which reduces its level of salinity. <br>
                In most oceans, the salinity is balanced by evaporation and open connections with other bodies of water.
                <br>
                But this is not true. With regard to the Arctic Ocean. <br>
                In fact, the Arctic Ocean is connected to other oceans only via the Bering Strait (to the Pacific Ocean)
                and the Greenland and Labrador Seas (to the Atlantic Ocean). <br>
                In addition, this rate of evaporation is very slow. <br>
                All of these factors contribute to giving the Arctic Ocean the lowest level of salinity of any ocean, In
                the world.
            </p>
            <h3>
                Wildlife in the Arctic Ocean:
            </h3>
            <p>
                Despite the harsh climatic conditions found around the Arctic Ocean, it is still home to a wide variety
                of wildlife. <br>
                Some of the most common animals that live in the Arctic Ocean include walruses, polar bears, beluga
                whales, beluga whales, Arctic wolves, and walleye whales. <br>
                For example, polar bears depend on the thick ice covering the Arctic Ocean to reach their primary prey,
                seals. <br>
                As this ice has become thinner in recent years, it has become increasingly difficult to obtain their
                main food source.
            </p>
            <h3>
                continental shelf:
            </h3>
            <p>
                The Arctic Ocean is home to the largest continental shelf in the world, extending about 750 miles
                towards Siberia and contributing to the formation of the previously mentioned islands. <br>
                They believe that this shelf contains large deposits of oil and natural gas, which led to an
                international dispute over the regional ownership of the area.
            </p>
