            <h3>
                Currents:
            </h3>
            <p>
                The Antarctic Circumpolar Currents are always moving eastward - chasing and joining themselves at 21,000
                km (13,000 mi) in length - and include the world's longest ocean current, transporting 130 million cubic
                meters (4.6 billion cubic feet) of water per second - 100 times more than the flow of rivers, The whole
                world.
            </p>
            <h3>
                The climate:
            </h3>
            <p>
                Sea temperatures vary from approximately -2 to 10 degrees Celsius (28 to 50 degrees Fahrenheit). <br>
                Hurricanes move eastward around the continent, often becoming intense due to the difference in
                temperature between the ice and the open ocean. <br>
                The ocean is located at latitude 40 south towards the Antarctic Circle, which has the strongest average
                winds of any place on Earth. <br>
                The ocean freezes in winter beyond 65°S in the Pacific sector and 55°S in the Atlantic sector, and
                surface temperatures fall well below 0°C. <br>
                Constant strong winds inland keep some coastal points free of ice throughout winter season. <br>
                In strong winds, that's why no one lived on the continent.
            </p>
