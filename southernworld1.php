            <h3>
                Introduction:
            </h3>
            <p>
                The Southern Ocean was a globally recognized body of water. <br>
                The International Hydrographic Organization (IHO) recognized this in 1937, but reversed the decision in
                1953 due to controversy over the exact boundaries of the oceans, according to National Geographic. <br>
                But scientists have long known about the Southern Ocean, and have confirmed that its unique qualities
                make it a distinct body of water with its own environmental and conservation concerns. <br>
                The largest marine protected area in the world is located in the Southern Ocean, in the Ross Sea, and
                was formed to protect the nutrient-rich waters that support... Krill (the most important ocean organism
                in the upper food chain is the small shrimp-like krill), plankton, penguins, whales, etc. <br>
                It is also called the Antarctic Ocean, it covers about one-sixteenth of the total ocean area on Earth.
                <br>
                The Southern Ocean consists of parts of the global ocean, the South Pacific Ocean, the Atlantic Ocean,
                the Indian Ocean, and their tributary seas surrounding Antarctica below 60 degrees south latitude. <br>
                It is not cut off from any other continental landmass. <br>
                Its narrowest passage is the Drake Passage, which is 600 miles (about 1,000 km) wide, between South
                America and the tip of the Antarctic Peninsula. <br>
                In terms of "area", the Arctic Ocean is Only he is smaller. <br>
                The Southern Ocean covers 8,479,000 square miles (21,960,000 square km), it contains 17,226,000 cubic
                miles (71,800,000 cubic kilometers) of water. <br>
                Its average depth is 10,728 feet (3,270 m). <br>
                It has a maximum depth of 24,383 feet (7,432 m) in the South Sandwich Trench (3,270 m), southeast of
                South Georgia Island.
                Most oceans are bordered by continents, and this continent surrounds Antarctica and is bordered by three
                other oceans. <br>
                But the waters of the Southern Ocean are not just an extension of those oceans. <br>
                What makes the Southern Ocean so special is due in large part to the Antarctic Circumpolar Current
                (ACC), a 34-million-year-old current that is cooler and less salty than the waters immediately north of
                it. <br>
                These properties make ACC a powerful engine for global water and heat cycling.
            </p>
