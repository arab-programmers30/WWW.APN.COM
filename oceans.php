            <h1>The oceans on planet Earth</h1>
            <p>
                The workings of the oceans include global systems that make the Earth habitable for humans. <br>
                Rainwater, drinking water, weather, climate, coastlines, much of our food,
                and even the oxygen in the air we breathe are provided and regulated by the seas. <br>
                Therefore, carefully managing these essential global resources is one of the key features of a
                sustainable future. <br>
                However, currently, there is a continuing degradation of coastal waters due to pollution,
                and ocean acidity is having an adverse impact on the functioning of ecosystems and biodiversity,
                and this is also negatively affecting small-scale fisheries. <br>
                Saving our ocean must remain a priority. <br>
                Marine biodiversity is critical to the health of people and the health of our planet. <br>
                We must effectively manage marine protected areas, provide them with adequate resources,
                and establish the necessary regulations to reduce overfishing, marine pollution, and ocean
                acidification. <br>
                Ocean conservation and action should not stop as we address the COVID-19 pandemic. We need to look at
                long-term solutions to maintain the health of our planet as a whole. Our lives depend on a healthy
                planet.
                The health of the oceans is closely linked to our health. <br>
                <br>
                The ocean makes up 70% of the Earth's surface, and up to 90-95% of the Earth's habitable volume. <br>
                Half of net primary production (the rate of new biomass on Earth, e.g., leaf growth) comes from marine
                phytoplankton (single-celled, plant-like organisms that perform photosynthesis). <br>
                These phytoplankton form the base of the entire ocean ecosystem; Not only do they provide food for other
                animals in the sea and oxygen for us to breathe, but they also absorb carbon dioxide and enable carbon
                sequestration. <br>
                Many people do not realize the huge role the ocean plays in our daily lives. <br>
                Limiting global warming to 1.5°C would require removing approximately 100-1000 gigatonnes of carbon
                dioxide during the period 2000-2100. <br>
                Currently, phytoplankton remove between 5-12 gigatonnes of carbon dioxide carbon dioxide annually. <br>
                <br>
                According to UNESCO, the ocean can be an ally against Covid-19: <br>
                Bacteria found in the depths of the ocean are being used to make a rapid test to detect the presence of
                Covid-19. <br>
                The diversity of ocean life holds great promise for pharmaceuticals. <br>
                The pandemic provides an opportunity to revitalize the oceans and begin building a sustainable ocean
                economy. <br>
                A report by the United Nations Economic and Social Commission for Asia and the Pacific suggests that
                factors such as temporary closures of activities,
                reduced human movement and demands on resources due to the COVID-19 pandemic may provide marine
                environments with the breathing space they desperately need to begin recovery. <br>
                The Ocean Conference, scheduled for June 2020, has been postponed to a later date due to the COVID-19
                pandemic.
            </p>
