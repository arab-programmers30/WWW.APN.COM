                Rasha Adel
            </h3>
            <h4>
                Source: NASA's Goddard Space Flight Center in Greenbelt, Md.
            </h4>
            <h3>
                NASA Examines El Niño's Impact on Ocean’s Food Source
            </h3>
            <p>
                El Niño years can have a big impact on the littlest plants in the ocean, and NASA scientists are
                studying the relationship between the two.
                In El Niño years, huge masses of warm water – equivalent to about half of the volume of the
                Mediterranean Sea – slosh east across the Pacific Ocean towards South America. While this warm water
                changes storm systems in the atmosphere, it also has an impact below the ocean’s surface. These impacts,
                which researchers can visualize with satellite data, can ripple up the food chain to fisheries and the
                livelihoods of fishermen.
                Strong El Nino events have a big impact on phytoplankton (in green), especially when the warm water
                pushes far to the east of the Pacific Ocean, as in 1997.
            </p>
            <h3>
                Credits: NASA/Goddard
            </h3>
            <p>
                El Niño’s mass of warm water puts a lid on the normal currents of cold, deep water that typically rise
                to the surface along the equator and off the coast of Chile and Peru, said Stephanie Uz, ocean scientist
                at Goddard Space Flight Center in Greenbelt, Maryland. In a process called upwelling, those cold waters
                normally bring up the nutrients that feed the tiny organisms, which form the base of the food chain.
                "An El Niño basically stops the normal upwelling," Uz said. "There’s a lot of starvation that happens to
                the marine food web." These tiny plants, called phytoplankton, are fish food – without them, fish
                populations drop, and the fishing industries that many coastal regions depend on can collapse.
                El Niño years can have a big impact on the littlest plants in the ocean, and NASA scientists are
                studying the relationship between the two. Ocean color maps, based on a month's worth of satellite data,
                show El Niño's impact on phytoplankton.
                This video is public domain and can be downloaded at the Scientific Visualization Studio
                With NASA satellite data, and ocean color software called SeaDAS, developed at the Ocean Biology
                Processing Group at Goddard, Uz has been mapping where these important phytoplankton appear. Orbiting
                instruments like the Moderate Resolution Imaging Spectrometer on the Aqua satellite, and the Visible
                Infrared Imaging Radiometer Suite on the Suomi NPP satellite collect data on the color of the ocean.
                From shades of blue and green, scientists can calculate the amount of green chlorophyll – and therefore
                the amount of phytoplankton present.
                The ocean color maps, based on a month’s worth of satellite data, can show that El Niño impact on
                phytoplankton. In December 2015, at the peak of the current El Niño event, there was more blue – and
                less green chlorophyll – in the Pacific Ocean off of Peru and Chile, compared to the previous year. Uz
                and her colleagues are also watching as the El Niño weakens this spring, to see when and where the
                phytoplankton reappear as the upwelling cold water brings nutrients back to the region.
                "They can pop back up pretty quickly, once they have a source of nutrients," Uz said.
                Researchers can also examine the differences in ocean color between two different El Niño events. During
                the large 1997-1998 El Niño event, the green chlorophyll virtually disappeared from the coast of Chile.
                This year’s event, while it caused a drop in chlorophyll primarily along the equator, was much less
                severe for the coastal phytoplankton population. The reason – the warmer-than-normal waters associated
                with the two El Niño events were centered in different geographical locations. In 1997-1998, the biggest
                ocean temperature abnormalities were in the eastern Pacific Ocean; this year the focus was in the
                central ocean. This difference impacts where the phytoplankton can feed on nutrients, and where the fish
                can feed on phytoplankton.
                "When you have an East Pacific El Niño, like 1997-1998, it has a much bigger impact on the fisheries off
                of South America," Uz said.  But Central Pacific El Niño events, like this year’s, still have an impact
                on ocean ecosystems, just with a shift in location. Researchers are noting reduced food available along
                the food chain around the Galapagos Islands, for example. And there has been a drop in phytoplankton off
                the coast of South America, just not as dramatically as before.
                Differences in December phytoplankton abundances are visualized for three years: during the strong East
                Pacific El Nino of 1997 (using SeaWiFS satellite data), during a normal year in 2013 (using data from
                MODIS on the Aqua satellite), and during the strong Central Pacific El Nino of 2015 (MODIS/Aqua).
            </p>
            <h3>
                Credits: Uz/NASA Goddard
            </h3>
            <p>
                Scientists have more tools on hand to study this El Niño, and can study more elements of the event, Uz
                said. They’re putting these tools to use to ask questions not just about ocean ecology, but about the
                carbon cycle as well.
                "We know how important phytoplankton are for the marine food web, and we’re trying to understand their
                role as a carbon pump," Uz said. The carbon pump refers to one of the ways the Earth system removes
                carbon dioxide from the atmosphere. When phytoplankton die, their carbon-based bodies sink to the ocean
                floor, where they can remain for millions of years. El Niño is a naturally occurring disruption to the
                typical ocean currents, she said – so it’s important to understand the phenomenon to better attribute
                what occurs naturally, and what occurs due to human-caused disruptions to the system.
                Other scientists at Goddard are investigating ways to forecast the ebbs and flows of nutrients using the
                center’s supercomputers, incorporating data like winds, sea surface temperatures, air pressures and
                more.
                "It’s like weather forecasts, but for bionutrients and phytoplankton in the ocean," said Cecile
                Rousseaux, an ocean modeler with Goddard’s Global Modeling and Assimilation Office. The forecasts could
                help fisheries managers estimate how good the catch could be in a particular year, she said, since fish
                populations depend on phytoplankton populations. The 1997-1998 El Niño led to a major collapse in the
                anchovy fishery off of Chile, which caused economic hardships for fishermen along the coast.
                So far, Rousseaux said, the phytoplankton forecast models haven’t shown any collapses for the 2015-2016
                El Niño, possibly because the warm water isn’t reaching as far east in the Pacific this time around. The
                forecast of phytoplankton populations effort is a relatively new effort, she said, so it’s too soon to
                make definite forecasts. But the data so far, from the modeling group and others, show conditions
                returning to a more normal state this spring.
                The next step for the model, she said, is to try to determine which individual species of phytoplankton
                will bloom where, based on nutrient amounts, temperatures and other factors – using satellites and other
                tools to determine which kind of microscopic plant is where.
                "We rely on satellite data, but this will go one step further and give us even more information,"
                Rousseaux said.
            </p>
